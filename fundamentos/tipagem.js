/* 
* JS possui tipagem fraca, ou seja, 
* não precisamos declarar qual o tipo
* da variável que declaramos.
* 
* Além disso o tipo da váriavel pode 
* ser alterado automáticamente ao decorrer
* do código.
*/

let pi = 3.14;
console.log(pi);
console.log(typeof(pi)+"\n");

pi = `três vírgula quatorze`;
console.log(pi);
console.log(typeof(pi));

